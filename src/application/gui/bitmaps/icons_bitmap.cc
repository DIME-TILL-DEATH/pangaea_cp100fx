const uint8_t strelk[] =
{
		0x60, 0x50, 0x48, 0x44, 0x42, 0x41,    //UP
		0x42, 0x44, 0x48, 0x50, 0x60, 0x00, 0x06, 0x0a, 0x12, 0x22, 0x42, 0x82,    //DOWN
		0x42, 0x22, 0x12, 0x0a, 0x06, 0x00
};

icon_t iconFormMenuType(gui_menu_type menuType)
{
	return (icon_t)(menuType - MENU_DELAY);
}

void icon_print(icon_t num, strelka_t strel)
{
	uint8_t col = 111;
	uint16_t addr = num * 34;

	uint8_t nullBuf[12];
	kgp_sdk_libc::memset(nullBuf, 0, 12);

	for(int i = 0; i < 2; i++)
	{
		LCD_SetPageAddress(i + 1);
		LCD_SetColumnAddress(col);
		LCD_WriteData(&icon_bit[addr], 17);
		addr += 17;
	}
	col += 3;

	switch(strel)
	{
		case 0:
		break;
		case 1:
		{
			arrow_print(col, 0, 1);

			LCD_SetColumnAddress(col);
			LCD_SetPageAddress(3);
			LCD_WriteData(nullBuf, 12);
			break;
		}
		case 2:
		{
			LCD_SetColumnAddress(col);
			LCD_SetPageAddress(0);
			LCD_WriteData(nullBuf, 12);

			arrow_print(col, 3, 0);
			break;
		}
		case 3:
		{
			arrow_print(col, 0, 1);
			arrow_print(col, 3, 0);
			break;
		}
	}
}

void arrow_print(uint8_t col, uint8_t pag, uint8_t dir)
{
	LCD_SetColumnAddress(col);
	LCD_SetPageAddress(pag);

	if(dir)
		LCD_WriteData(&strelk[0], 12);
	else
		LCD_WriteData(&strelk[12], 12);
}

