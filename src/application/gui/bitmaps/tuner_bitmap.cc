void tuner_scale(void)
{
	LCD_SetColumnAddress(0);
	LCD_SetPageAddress(2);
	GPIO_ResetBits(GPIOB,CS);
	for(uint8_t i = 0 ; i < 127 ; i++)
	{
		if(i < 64)
			LCD_WriteData(scale_tun[i]);
		else
			LCD_WriteData(scale_tun[126 - i]);
	}
	GPIO_SetBits(GPIOB,CS);
}


